package com.shpp.p2p.cs.ibilash.assignment12;

/**
 * Vertex with x,y coordinate
 */
class Vertex {
    private int x;
    private int y;

    Vertex(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * get vertex x
     *
     * @return vertex x
     */
    public int getX() {
        return x;
    }

    /**
     * get vertex y
     *
     * @return vertex y
     */
    public int getY() {
        return y;
    }
}
